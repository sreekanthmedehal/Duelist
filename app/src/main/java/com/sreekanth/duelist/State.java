package com.sreekanth.duelist;

/*
 * Basic Data class to hold a state name and the state capital.
 */
public class State {
	
	private String name;
	private String polno;
	

	
	private String plan;
	private String premdue;
	private String dueriskdate;
	private String cbo;
	private String adjdate;
	private String prem;
	private String comm;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPolno() {
		return polno;
	}
	public void setPolno(String polno) {
		this.polno = polno;
	}
	public String getPlan() {
		return plan;
	}
	public void setPlan(String plan) {
		this.plan = plan;
	}
	public String getPremdue() {
		return premdue;
	}
	public void setPremdue(String premdue) {
		this.premdue = premdue;
	}
	public String getDueRiskDate() {
		return dueriskdate;
	}
	public void setRiskDate(String dueriskdate) {
		this.dueriskdate = dueriskdate;
	}
	public String getCbo() {
		return cbo;
	}
	public void setCbo(String cbo) {
		this.cbo = cbo;
	}
	public String getAdjdate() {
		return adjdate;
	}
	public void setAdjdate(String adjdate) {
		this.adjdate = adjdate;
	}
	public String getPrem() {
		return prem;
	}
	public void setPrem(String prem) {
		this.prem = prem;
	}
	public String getComm() {
		return comm;
	}
	public void setComm(String comm) {
		this.comm = comm;
	}
}
